package string;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 234.4556f;
        System.out.printf("Formated number: %.2f", a);
        System.out.println();
        System.out.printf("Pie: %.4f", Math.PI);
        System.out.println();
        System.out.printf("Hello, My name is %s and I am %s", "Gaurav", "Engineer");

//        For more String print format - https://www.digitalocean.com/community/tutorials/java-printf-method
    }
}
